package com.wplab.memberjoin;

public class MemberInfo {
	private String name;
	private String id;
	private String passwd;
	private String gender;
	private String inotice;
	private String cnotice;
	private String dnotice;
	private String job;
	
	public MemberInfo() {
		
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getInotice() {
		return inotice;
	}
	public void setInotice(String inotice) {
		this.inotice = inotice;
	}
	public String getCnotice() {
		return cnotice;
	}
	public void setCnotice(String cnotice) {
		this.cnotice = cnotice;
	}
	public String getDnotice() {
		return dnotice;
	}
	public void setDnotice(String dnotice) {
		this.dnotice = dnotice;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}

}
